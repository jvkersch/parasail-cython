import unittest

import cyparasail as parasail


class TestAutogenerated(unittest.TestCase):

    def test_assert_callable(self):
        for func in FUNC_NAMES:
            self._import_and_call(func)

    def _import_and_call(self, funcname):
        func = getattr(parasail, funcname)
        s1 = "QQEGIL"
        s2 = "QQQERGII"
        res = func(s1, s2, 10, 1, parasail.pam10)
        self.assertIsNotNone(res)


